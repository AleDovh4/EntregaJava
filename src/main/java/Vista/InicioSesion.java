    public boolean buscarUsuario(String usuario, String password)
    {
        String query;
        try{
            Statement s = link.createStatement();
            query="select * from Usuario where NombreUsuario='"+usuario+"' AND Password ='"+password+"'";
            ResultSet rs=s.executeQuery(query);
            if(rs.next())
            {
                String nombreUsuario =rs.getString("NombreUsuario");
                String contra =rs.getString("Password");
                if(nombreUsuario.equals(usuario) && contra.equals(password))
                {
                    return true;
                }
            }
        }catch(SQLException ex){
            Logger.getLogger(Conexion.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }
    
